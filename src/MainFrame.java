    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        passwd = String.valueOf(pass1field.getPassword());
        
        String pass2 = String.valueOf(pass2field.getPassword());
        pass1field.setText(null);
        pass2field.setText(null);
        
        if(!passwd.equals(pass2)){
            JOptionPane.showMessageDialog(null, "Contraseñas distintas","Error",JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            registerTextField.setText("SI");
            registerDialog.setVisible(false);
            loginButton.setEnabled(true);
        }
    }//GEN-LAST:event_acceptButtonActionPerformed

