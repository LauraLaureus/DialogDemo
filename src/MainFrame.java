    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        if(isLoging){
            int retorno = JOptionPane.showConfirmDialog(null, "¿Realmente quiere salir?","LogOut", JOptionPane.YES_NO_OPTION);
            if (retorno == 0){
                isLoging = !isLoging;
                loginButton.setText("LogIn");
                loginTextField.setText("NO");
            }
        }else{
            loginDialog.pack();
            loginDialog.setVisible(true);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String pass = String.valueOf(loginPasswordField.getPassword());
        loginPasswordField.setText(null);
        if(pass.equals(passwd)){
            loginTextField.setText("SI");
            isLoging = !isLoging;
            loginButton.setText("LogOut");
            loginDialog.setVisible(false);
            registerButton.setEnabled(false);
        }else{
            if (loginAttempts < 3){
                loginAttempts +=1;
                JOptionPane.showMessageDialog(null, "Constraseña incorrecta.\n Inténtelo de nuevo\n Intentos restantes: "+(4-loginAttempts), "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Intentos de inicio de sesión excedido.\n Se borrará la contraseña.\n Regístrese de nuevo.", "Error", JOptionPane.ERROR_MESSAGE);
                passwd = null;
                registerTextField.setText("NO");
                isLoging = false;
                loginButton.setEnabled(false);
                loginDialog.setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

